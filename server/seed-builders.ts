export async function seedBuilders() {
  try {
    console.log("ðŸŒ± Starting builder and service seeding...");

    // Insert builders
    const insertedBuilders = [];
    for (const builderData of sampleBuilders) {
      const [builder] = await db.insert(builders).values(builderData).returning();
      insertedBuilders.push(builder);
      console.log(`âœ… Created builder: ${builder.name}`);
    }

    // Insert services and link to builders
    for (let i = 0; i < sampleServices.length; i++) {
      const serviceData = {
        ...sampleServices[i],
        builderId: insertedBuilders[i].id,
      };
      const [service] = await db.insert(services).values(serviceData).returning();
      console.log(`âœ… Created service: ${service.title}`);
    }

    console.log("ðŸŽ‰ Successfully seeded builders and services!");
    return { success: true, buildersCount: insertedBuilders.length, servicesCount: sampleServices.length };
  } catch (error) {
    console.error("âŒ Error seeding builders:", error);
    throw error;
  }
}

// Run if executed directly
